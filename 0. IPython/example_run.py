import numpy as np

def add_two_nums(a, b):
    """
    Adds three numbers and returns its value.
    
    Args
    -----
    a (int): Number one.
    b (int): Number two.
    
    Returns
    -------
    type: float
    
    """
    c = np.random.randn()
    
    return a + b + c

